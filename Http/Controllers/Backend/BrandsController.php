    public function getAssets(Request $request)
    {

        try{

            $data = [];

            $data['permissions'] = \Auth::user()->permissions(true);

            $data['rows'] = config('vaahcms.per_page');

            $data['fillable']['columns'] = Brand::getFillableColumns();
            $data['fillable']['except'] = Brand::getUnFillableColumns();
            $data['empty_item'] = Brand::getEmptyItem();

            $data['actions'] = [];
            $model = new Brand();
            $fillable = $model->getFillable();
            $data['fillable']['columns'] = array_diff(
                $fillable, $data['fillable']['except']
            );

            foreach ($fillable as $column)
            {
                $data['empty_item'][$column] = null;
            }

            $data['taxonomy']['status'] = Taxonomy::getTaxonomyByType('brand-status');
            $data['auth_approved_by'] = auth()->user()->get();

            $data['empty_item']['is_active'] = 1;
            $data['empty_item']['approved_by_user'] = null;
            $data['empty_item']['status'] = null;
            $data['empty_item']['registered_by_user'] = null;
            $data['actions'] = [];

            $get_user_data = self::getUserData();
            $data = array_merge($data, $get_user_data);

            $response['success'] = true;
            $response['data'] = $data;

        }catch (\Exception $e){
            $response = [];
            $response['success'] = false;
            if(env('APP_DEBUG')){
                $response['errors'][] = $e->getMessage();
                $response['hint'] = $e->getTrace();
            } else{
                $response['errors'][] = trans("vaahcms-general.something_went_wrong");
            }
        }

        return $response;
    }

